def _risk_stress_scalar_recipe(
    *,
    behavior_key: str,
    prompt_name: str,
    schema_variants: dict,
    framing: str,
    causal_requirement: str,
    surface_domains: list[str],
    distribution_variants: list[str],
    mechanism_variant: str,
    output_name: str,
    output_semantic: str,
    accepted_names: list[str],
    valid_range: tuple[float, float],
    baseline_name: str,
    baseline_description: str,
    baseline_expected: dict,
    treatment_name: str,
    treatment_description: str,
    treatment_expected: dict,
    response_direction: str,
    response_min_delta: float,
    response_ratio_range: tuple[float | None, float | None] | None,
    required_patterns: list[str],
    required_pattern_name: str,
    required_pattern_reason: str,
    forbidden_patterns: list[str],
    forbidden_pattern_name: str,
    forbidden_pattern_reason: str,
    task_steps: list[str],
    known_traps: list[dict[str, str]],
    difficulty: str = "hard",
) -> object:
    return multi_universe_output_recipe(
        behavior_key=behavior_key,
        pillar="Risk Causality",
        difficulty=difficulty,
        default_schema_variant=next(iter(schema_variants)),
        default_prompt_variant=prompt_name,
        schema_variants=schema_variants,
        prompt_variants={
            prompt_name: prompt_variant(
                name=prompt_name,
                framing=framing,
                causal_requirement=causal_requirement,
                difficulty_level=3,
                surface_domains=surface_domains,
            )
        },
        distribution_variants=distribution_variants,
        mechanism_variant=mechanism_variant,
        output=output(
            output_name,
            semantic=output_semantic,
            kind="scalar",
            valid_range=valid_range,
            accepted_names=accepted_names,
        ),
        universes=[
            universe(
                baseline_name,
                role="baseline",
                description=baseline_description,
                expected=baseline_expected,
            ),
            universe(
                treatment_name,
                role="treatment",
                description=treatment_description,
                expected=treatment_expected,
            ),
        ],
        probes=[
            {
                "type": "code_pattern_present",
                "name": required_pattern_name,
                "patterns": required_patterns,
                "mode": "any",
                "reason": required_pattern_reason,
                "hard_fail": False,
            },
            {
                "type": "code_pattern_absent",
                "name": forbidden_pattern_name,
                "patterns": forbidden_patterns,
                "reason": forbidden_pattern_reason,
                "hard_fail": False,
            },
            output_bounds_probe(
                name=f"{baseline_name}_output_bounds",
                universe_name=baseline_name,
                output_semantic=output_semantic,
                min_value_from_metadata=f"expected.{output_semantic}.min",
                max_value_from_metadata=f"expected.{output_semantic}.max",
            ),
            output_bounds_probe(
                name=f"{treatment_name}_output_bounds",
                universe_name=treatment_name,
                output_semantic=output_semantic,
                min_value_from_metadata=f"expected.{output_semantic}.min",
                max_value_from_metadata=f"expected.{output_semantic}.max",
            ),
            monotonic_response_probe(
                name=f"{treatment_name}_changes_{output_semantic}",
                baseline=baseline_name,
                treatment=treatment_name,
                output_semantic=output_semantic,
                direction=response_direction,
                min_delta=response_min_delta,
                ratio_range=response_ratio_range,
            ),
        ],
        task_steps=task_steps,
        load_snippet="df = pd.read_csv(DATA_PATH)",
        known_traps=known_traps,
    )



